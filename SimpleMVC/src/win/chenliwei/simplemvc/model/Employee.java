package win.chenliwei.simplemvc.model;

public class Employee {
	private String name;
	private float Salary;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getSalary() {
		return Salary;
	}
	public void setSalary(float salary) {
		Salary = salary;
	}
	

}
